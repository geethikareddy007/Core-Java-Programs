class AlphabetOrNot{   
	public static void main(String[] args){
		char ch='&';
		if((ch>='A'&&ch<='Z')||(ch>'a'&&ch<='z'))
			System.out.println(ch+" is a Alphabet");
		else
			System.out.println(ch+" is not a Alphabet");	//& is not a Alphabet
	}
}
	
	
	
	
