class SpecialCharacter{   
	public static void main(String[] args){
		char ch='$';
		if((ch>='A'&&ch<='Z')||(ch>'a'&&ch<='z')||(ch>='0'&&ch<='9'))
			System.out.println(ch+" is not a SpecialCharacter");
		else
			System.out.println(ch+" is a SpecialCharacter");	//$ is a SpecialCharacter	
	}
}





