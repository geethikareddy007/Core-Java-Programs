class AsciiEvenOdd{   
	public static void main(String[] args){
		char ch='B';
		if(ch%2==0)
			System.out.println(ch+" Ascii Value is Even");	//B Ascii Value is Even
		else
			System.out.println(ch+" Ascii Value is Odd");
	}
}
	
	
	
	
	
	
